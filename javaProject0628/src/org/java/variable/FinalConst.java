package org.java.variable;

public class FinalConst {
	public static void main(String[] args) {
		System.out.println("wazzupppp");
		
		// final constant
		final int MAXNUM = 100;

		final String PROJECTNAME = "java";
		System.out.println(MAXNUM + " " + PROJECTNAME + " classes");
		
		final double PI = 3.14;
		
		int radius = 9;
		
		System.out.println(radius * radius * PI);
	}
}
